train.jags <- function(data,
                       model     = "galaxy.jags",
                       chains    = 8,
                       nodes     = 8,
                       burn      = 500,
                       iter      = 1000) {
    
    system.time(m <- jags.parallel(
        model.file          = model,
        data                = d,
        n.chains            = chains,
        n.cluster           = nodes,
        n.burnin            = burn,
        parameters.to.save  = c('dens', 'cl',
                                "deviance", "pD",
                                "p.val"),
        jags.module         = c("dic"),
        n.iter              = iter,
        DIC                 = TRUE,
        n.thin              = 2
    ))
    
    m
}